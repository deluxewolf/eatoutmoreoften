	private void loadStats(){
		jLabel8.setText("Total Orders: " + getCount("orders.txt"));
		jLabel9.setText("Total Customers: " + getCount("customers.txt"));
		jLabel10.setText("Total Complaints: " + getCount("complaints.txt"));
		jLabel12.setText("Total Users: " + getCount("users.txt"));
	}

	// gets statistic numbers
	private int getCount(String filename){
		int counts = 0;
		
		try {
			FileReader fr = new FileReader(filename);
			BufferedReader br = new BufferedReader(fr);
			while(br.ready()) {
				br.readLine();
				counts++;
			}
			br.close();
			fr.close();
		}
		catch(IOException e) {
			// no error message needed if file cannot be read!
		}
		
		return counts;
	}
